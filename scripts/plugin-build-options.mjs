export const pluginBanner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

export const CANONICAL_API_BASE_URL = 'https://api.systemsculpt.com/api/v1';
export const CANONICAL_WEBSITE_API_BASE_URL = 'https://systemsculpt.com/api/plugin';

export const builtinExternals = [
  'node:*',
  'child_process', 'crypto', 'fs', 'fs/promises', 'http', 'https',
  'os', 'path', 'process', 'stream', 'stream/web', 'url', 'util',
];

export function normalizeApiBaseUrl(value = CANONICAL_API_BASE_URL) {
  const raw = String(value || '').trim().replace(/\/+$/, '');
  let parsed;
  try {
    parsed = new URL(raw);
  } catch {
    throw new Error('SYSTEMSCULPT_API_BASE_URL must be an absolute HTTP(S) URL');
  }
  if (
    !['http:', 'https:'].includes(parsed.protocol) ||
    parsed.username ||
    parsed.password ||
    parsed.search ||
    parsed.hash
  ) {
    throw new Error(
      'SYSTEMSCULPT_API_BASE_URL must be an absolute HTTP(S) URL without credentials, query, or hash',
    );
  }
  return raw;
}

export function resolveWebsiteApiBaseUrl({
  apiBaseUrl = CANONICAL_API_BASE_URL,
  websiteApiBaseUrl,
} = {}) {
  if (String(websiteApiBaseUrl || '').trim()) {
    return normalizeApiBaseUrl(websiteApiBaseUrl);
  }

  const normalizedApiBaseUrl = normalizeApiBaseUrl(apiBaseUrl);
  const parsed = new URL(normalizedApiBaseUrl);
  const hostname = parsed.hostname.toLowerCase();
  const loopback = hostname === 'localhost'
    || hostname === '0.0.0.0'
    || hostname === '::1'
    || /^127(?:\.\d{1,3}){3}$/.test(hostname);
  if (loopback) {
    return `${parsed.origin}/api/plugin`;
  }
  return CANONICAL_WEBSITE_API_BASE_URL;
}

export function createPluginBuildOptions({
  entryPoint = 'src/main.ts',
  outfile = 'main.js',
  write = true,
  production = true,
  overrides = {},
  plugins = [],
  buildStamp = new Date().toISOString(),
  apiBaseUrl = CANONICAL_API_BASE_URL,
  websiteApiBaseUrl,
} = {}) {
  const normalizedApiBaseUrl = normalizeApiBaseUrl(apiBaseUrl);
  const normalizedWebsiteApiBaseUrl = resolveWebsiteApiBaseUrl({
    apiBaseUrl: normalizedApiBaseUrl,
    websiteApiBaseUrl,
  });
  return {
    banner: { js: pluginBanner },
    entryPoints: [entryPoint],
    bundle: true,
    alias: {
      'onnxruntime-node': 'onnxruntime-web',
    },
    define: {
      '__SS_BUILD_STAMP__': JSON.stringify(buildStamp),
      '__SYSTEMSCULPT_API_BASE_URL__': JSON.stringify(normalizedApiBaseUrl),
      '__SYSTEMSCULPT_WEBSITE_API_BASE_URL__': JSON.stringify(normalizedWebsiteApiBaseUrl),
    },
    external: [
      'obsidian', 'electron', 'proper-lockfile', 'graceful-fs',
      '@codemirror/autocomplete', '@codemirror/collab', '@codemirror/commands',
      '@codemirror/language', '@codemirror/lint', '@codemirror/search',
      '@codemirror/state', '@codemirror/view', '@lezer/common', '@lezer/highlight', '@lezer/lr',
      ...builtinExternals,
    ],
    format: 'cjs',
    target: 'es2018',
    logLevel: 'error',
    sourcemap: production ? false : 'inline',
    treeShaking: true,
    outfile,
    write,
    loader: { '.wasm': 'file' },
    plugins,
    ...overrides,
  };
}

import builtins from 'builtin-modules';

export const pluginBanner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
if (typeof globalThis.process === "undefined") {
  globalThis.process = { env: {}, platform: "", version: "v20.0.0", cwd: function() { return "/"; }, pid: 0 };
}
const __systemsculpt_import_meta_url__ = (() => {
  const filename = typeof __filename === "string" ? __filename : "";
  const normalizedPath = filename.replace(/\\\\/g, "/");
  const absolutePath = normalizedPath.startsWith("/") ? normalizedPath : "/" + normalizedPath;
  return encodeURI("file://" + absolutePath);
})();
`;

export const builtinExternals = Array.from(new Set(
  builtins.flatMap(name => {
    const normalized = String(name || '').replace(/^node:/, '');
    return normalized ? [normalized, `node:${normalized}`] : [];
  })
));

export function createPluginBuildOptions({
  entryPoint = 'src/main.ts',
  outfile = 'main.js',
  write = true,
  production = true,
  overrides = {},
  plugins = [],
  buildStamp = new Date().toISOString(),
} = {}) {
  return {
    banner: { js: pluginBanner },
    entryPoints: [entryPoint],
    bundle: true,
    alias: {
      'onnxruntime-node': 'onnxruntime-web',
      '@mariozechner/pi-tui': './src/services/pi/pi-tui-shim.js',
    },
    define: {
      'import.meta.url': '__systemsculpt_import_meta_url__',
      '__SS_BUILD_STAMP__': JSON.stringify(buildStamp),
    },
    external: [
      'obsidian', 'electron', 'proper-lockfile', 'graceful-fs',
      '@codemirror/autocomplete', '@codemirror/collab', '@codemirror/commands',
      '@codemirror/language', '@codemirror/lint', '@codemirror/search',
      '@codemirror/state', '@codemirror/view', '@lezer/common', '@lezer/highlight', '@lezer/lr',
      ...builtinExternals,
    ],
    format: 'cjs',
    target: 'es2018',
    logLevel: 'error',
    sourcemap: production ? false : 'inline',
    treeShaking: false,
    outfile,
    write,
    loader: { '.wasm': 'file' },
    plugins,
    ...overrides,
  };
}
